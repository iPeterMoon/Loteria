    private ImageIcon iconoActual;
    private static final int ANCHO_ICON = 140;
    private static final int ALTO_ICON = 140;

    /**
     * Creates new form PanelAvatar
     */
    public PanelAvatar() {
        actualizarAvatar(1);
        initComponents();
    }

    @Override
    protected void paintComponent(Graphics g) {
        super.paintComponent(g);
        if (iconoActual != null) {
            g.drawImage(iconoActual.getImage(), 0, 0, getWidth(), getHeight(), this);
        }
    }

    private ImageIcon cargarAvatar(int numero) {
        String url = "/avatars/" + numero + ".jpg";
        URL recurso = getClass().getResource(url);
        if (recurso == null) {
            System.err.println("No se encontr√≥ la imagen: " + url);
        }
        ImageIcon original = new ImageIcon(recurso);
        Image imagenEscalada = original.getImage().getScaledInstance(ANCHO_ICON, ALTO_ICON,
                Image.SCALE_SMOOTH);

        return new ImageIcon(imagenEscalada);
    }

    public void actualizarAvatar(int avatar) {
        iconoActual = cargarAvatar(avatar);
    }

